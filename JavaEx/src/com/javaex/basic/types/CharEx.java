package com.javaex.basic.types;

public class CharEx {

	public static void main(String[] args) {
		// Char(2)
		// Unicode Character
		
		char c1 = 'A', c2 = '한';
		
		System.out.println("c1 = " + c1);
		System.out.println("c2 = " + c2);
		
		System.out.println(c1 + c2);
		
		String str1 = "A", str2 = "한";
		
		System.out.println(str1 + str2);
	}

}
